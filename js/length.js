//The .length property
//Using the .length property, we can define the length (number of characters) in the string.

const fact = 'I love web dev!';
console.log(`"${fact}" is a string and it is length is ${fact.length}.`);

// "I love web dev!" is a string, and its length is 15.

//As you can see, the empty spaces between the words count as characters, as well as the exclamation mark at the end of the sentence.
// Make sure you are not using () after the .length

